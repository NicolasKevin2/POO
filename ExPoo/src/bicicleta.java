import java.lang.String;
public class bicicleta {
    private int id;
    private int nchassi;
    private String marca;
    private String modelo;
    private String cor;
    private String transmissão;
    private String amortecedor;
    private String freios;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getNchassi() {
        return nchassi;
    }

    public void setNchassi(int nchassi) {
        this.nchassi = nchassi;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public String getCor() {
        return cor;
    }

    public void setCor(String cor) {
        this.cor = cor;
    }

    public String getTransmissão() {
        return transmissão;
    }

    public void setTransmissão(String transmissão) {
        this.transmissão = transmissão;
    }

    public String getAmortecedor() {
        return amortecedor;
    }

    public void setAmortecedor(String amortecedor) {
        this.amortecedor = amortecedor;
    }

    public String getFreios() {
        return freios;
    }

    public void setFreios(String freios) {
        this.freios = freios;
    }

    public String getQuadro() {
        return quadro;
    }

    public void setQuadro(String quadro) {
        this.quadro = quadro;
    }

    private String quadro;
}
